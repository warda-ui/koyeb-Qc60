const mongoose = require('mongoose');

const complaintSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  nic: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
  subCategory: {
    type: String,
    required: true,
  },
  details: {
    type: String,
    required: true,
  },
  location: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    required: true,
  },
  time: {
    type: String,
    required: true,
  },
  files: [
    {
      fileName: { type: String, required: true },
      fileSize: { type: Number, required: true },
      filePath: { type: String, required: true },
    },
  ],
  
});

const Complaint = mongoose.model('Complaint', complaintSchema);

module.exports = Complaint;